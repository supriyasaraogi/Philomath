package com.advancese.Philomath;

public class ResetPass {

	String email;
	String password;
	
	public ResetPass(){
		
	}
	
	
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	
	
}
